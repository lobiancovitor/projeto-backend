public abstract class Pessoa {
    String nome;
    String sobrenome;
    String email;
    String telefone;
    String cpf;

    Pessoa(String nome, String sobrenome, String email, String telefone, String cpf){
        this.nome = nome;
        this.sobrenome = sobrenome;
        this.email = email;
        this.telefone = telefone;
        this.cpf = cpf;
    }

    
}
